package calculater;

public class calc {
	
	double plus(double a, double b) {
		return a+b;
	}
	double minus(double a, double b) {
		return a-b;
	}
	double multi(double a, double b) {
		return a*b;
	}
	double div(double a, double b) {
		return a/b;
	}
	double pow(double a,double b) {
		return Math.pow(a, b);
	}
	double sqrt(double a) {
		return Math.sqrt(a);
	}	
}
